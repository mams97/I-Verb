	<div class="row">
		<h2 class="text-warning text-center">Les temps des verbes en anglais</h2>
	</div>

	<div class="row mt-4 text-justify">
		<p>La langue anglaise est plus complexe qu'elle n'y parait. Comme la langue française, elle présente 3 temps de verbe principaux qui sont <b>le présent</b>, <b>le passé</b> et <b>le futur</b>. Toutefois, les temps anglais ne s’emploient pas forcément comme le français. Pensant bien faire, les francophones ont tendance à traduire mot à mot du français vers l’anglais, ce qui ne fonctionne pas à tous les coups.</p>
		<p>Je vous propose donc de reprendre un par un chacun des temps anglais et de voir dans quels cas les utiliser.</p>
	</div>

	<div class="row text-justify mt-5">
		<h3 class="fw-bold">Les temps du présent</h3>
		<div class="row">
			<h4 class="fw-bold text-primary">Le présent simple (PRESENT SIMPLE)</h4>
			<p>
				C’est le temps de base de la langue anglaise. Pour le former, rien de plus simple : il vous suffit de reprendre tel quel l’infinitif et d’y ajouter un -S uniquement à la 3e personne du singulier (surtout pas au pluriel !). Les verbes to go et to do deviennent respectivement goes et does à la 3e personne du singulier.</br>
				Prenons par exemple le verbe to want (vouloir) et conjuguons-le au présent simple.
			</p>
			<p>
				I want</br>
				You want</br>
				He/She wants</br>
				We want</br>
				You want</br>
				They want</br>
			</p>
			<p>
				Comme vous pouvez le voir, c’est incroyablement simple. Notons tout de même que si le verbe se termine par ss, il faut ajouter un e intercalaire avant le -S de la 3e personne du singulier. Par exemple : to kiss et he kissES. Pour les verbes qui se terminent par y, ce dernier devient ie devant la désinence -S : to cry et he cries.
			</p>
			<p>Voici comment former la négation et l’interrogation au présent simple :</p>
			<p>
				Négation : I do not want ou I don’t want</br>
				Interrogation : do I want?</br>
				Interro-négation : do I not want? ou don’t I want?</br>
			</p>	
			<p>
				Le présent simple est <b>le temps de la vérité générale</b>. Il désigne donc une action qui se répète dans le temps (I go there every Sunday) ou qui est toujours vraie (water boils at 100°C).
			</p>
		</div>
		<div class="row">
			<h4 class="fw-bold text-primary">Le présent continu (PRESENT CONTINUOUS)</h4>
			<p>
				C’est la fameuse forme en V+ing qui pose tant de problèmes aux francophones. Il se forme avec « être » (to be), suivi du participe présent, c’est-à-dire le verbe auquel on ajoute la terminaison -ing. Conjuguons, par exemple, le verbe to scream (crier).
			</p>
			<p>
				I am screaming</br>
				You are screaming</br>
				He/She is screaming</br>
				We are screaming</br>
				You are screaming</br>
				They are screaming</br>
			</p>
			<p>
				Négation : I am not screaming ou I’m not screaming</br>
				Interrogation : am I screaming?</br>
				Interro-négation : am I not screaming?</br>
			</p>
			<p>
				Encore une fois, ce n’est pas bien compliqué, il suffit de connaître la conjugaison du verbe to be. Attention cependant, le radical de certains verbes peut changer lorsque l’on forme le participe présent ou passé. Voici les cas qui posent problème :
				<ul class="ms-5">
					<li>Redoublement de la consonne : bet → betting, fit → fitting.</li>
					<li>Chute du e final  lorsqu’il est muet : take → taking, observe → observing.
						Mais pas toujours : age → ageing (en anglais britannique)
					</li>
					<li>Voyelle i qui devient y : lie → lying, tie → tying.</li>
					<li>Verbe dont le radical se termine par c : panic → panicking.</li>
				</ul>
			</p>
			<p>
				Le présent continu correspond tout d’abord à notre <i>« être en train de faire »</i>, il désigne donc <b>une action qui se produit au moment où l’on parle</b> et qui n’est donc pas terminée. « Je suis en train de manger une pomme » se dira en anglais I’m eating an apple. <br>
				Plus rarement, il permet de décrire une action qui n’a lieu qu’à un instant très précis et qui n’aurait pas cours autrement. Par exemple, You’re being foolish (tu es en train de te comporter comme un idiot), par opposition avec You’re foolish (tu es idiot).
			</p>
		</div>
		<h3 class="fw-bold">Les temps du passé</h3>
		<div class="row">
			<h4 class="fw-bold text-primary">Le passé simple (PRESENT SIMPLE or PRETERIT)</h4>
			<p>
				C’est la forme simple du passé qui se forme en ajoutant –ed au radical du verbe, sauf bien sûr s’il s’agit d’un verbe irrégulier.
			</p>
			<p>
				I wanted <br>
				you wanted <br>
				he / she wanted <br>
				we wanted <br>
				you wanted <br>
				they wanted <br>
			</p>
			<p>
				Négation : I did not want ou I didn’t want <br>
				Interrogation : did I want? <br>
				Interro-négation : did I not want? ou didn’t I want? <br>
			</p>
			<p>
				Il permet de <b>décrire une action passée et terminée</b>, qui s’est déroulée dans un contexte bien défini qui la rattache au passé. Voici quelques exemples :
			</p>
			<p class="ms-5">
				Exemple 1 : <i>He arrived yesterday</i> (il est arrivé hier) : <i>yesterday ancre l’action</i> dans le passé, il faut donc utiliser le passé simple.
			</p>
			<p class="ms-5">
				Exemple 2 : <i>I worked here from 2005 to 2008</i> (j’ai travaillé ici de 2005 à 2008) : le cadre temporel est une fois de plus défini et révolu, il impose donc l’usage du passé simple.
			</p>
			<p class="ms-5">
				Exemple 3 : <i>The Egyptians built the pyramids</i> (les Egyptiens construisirent les pyramides) : on place implicitement le contexte dans l’Egypte antique, donc dans un cadre temporel révolu.
			</p>
			<p>
				Le passé simple permet également de donner une caractéristique du sujet au moment où se déroule l’action, qui est à présent révolue.
			</p>
			<p class="ms-5">
				Exemple 4 : <i>I always ate in this restaurant</i> (je mangeais toujours dans ce restaurant) : ici, on comprend implicitement que le sujet ne mange plus dans ce restaurant.
			</p>
			<p class="ms-5">
				Exemple 5 : <i>It was a quiet night</i> (c’était une nuit tranquille) : on décrit une nuit en particulier, dans le passé.
			</p>
			<p class="ms-5">
				Exemple 6 : <i>She was a young woman</i> (c’était une jeune femme) : la femme en question était jeune au moment des faits.
			</p>
			<p>
				Vous avez sans doute pu vous en rendre compte en comparant les exemples et leur traduction, <b>les temps du français et de l’anglais ne coïncident pas toujours</b>. Ainsi, le passé simple anglais peut être traduit en utilisant notre passé simple, le passé composé ou encore l’imparfait. Prudence, donc !
			</p>
		</div>
		<div class="row">
			<h4 class="fw-bold text-primary">Le passé continu (PAST CONTINUOUS)</h4>
			<p>
				Comme son nom l’indique, c’est l’équivalent du présent continu… au passé. Il se forme donc avec le verbe to be au passé, was, accompagné du gérondif.
			</p>
			<p>
				I was screaming <br>
				you were screaming <br>
				he / she was screaming <br>
				we were screaming <br>
				you were screaming <br>
				they were screaming <br>
			</p>
			<p>
				Négation : I was not screaming ou I wasn’t screaming <br>
				Interrogation : was I screaming? <br>
				Interro-négation : was I not screaming? ou wasn’t I screaming? <br>
			</p>
			<p>
				Il permet de décrire une action qui s’est produite à un moment du passé, <b>en insistant sur la durée</b>. On l’utilise souvent dans une structure du type « passé continu + <b>when</b> + passé simple ».
			</p>
			<p class="ms-5">
				Exemple 1 : <i>I was playing the piano when she arrived in the room</i> (j’étais en train de jouer du piano lorsqu’elle arriva dans la pièce) : il y a un contraste entre l’action qui dure (jouer du piano) et celle qui n’arrive qu’à un moment précis (arriver dans la pièce).
			</p>
			<p class="ms-5">
				Exemple 2 : <i>I was playing chess the whole day</i> (j’ai joué aux échecs toute la journée) : on décrit une action qui a eu cours à un moment du passé et qui a duré un certain temps.
			</p>
		</div>
